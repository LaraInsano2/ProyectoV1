    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
       String Titulo = txtTema.getText();
    String Descripcion = txtDescripcion.getText();
    String Inicio = txtInicio.getText();
    String Fin = txtFin.getText();
     
    Proyectos t1 =new Proyectos();
    t1.setNOMBRE_PROYECTO(Titulo);
    t1.setDESCRIPCION_PROYECTO(Descripcion);
    t1.setFECHA_INICIO(Inicio);
    t1.setFECHA_FIN(Fin);
    
    CrearTema(t1);
     limpiarCampos();
     CrearProyecto.this.dispose();
    }//GEN-LAST:event_btnCrearActionPerformed

private void CrearTema(Proyectos t1){
       try {
    PersonaControlador controlador = new PersonaControlador();
       controlador.CrearProyecto(t1);
       limpiarCampos();
    } catch (Exception e) {
         JOptionPane.showMessageDialog(null, "Error al crear Tarea " + e.getMessage());
    }
}


private void limpiarCampos() {
    txtTema.setText("");
    txtDescripcion.setText("");
    txtInicio.setText("");
    txtFin.setText("");
}
